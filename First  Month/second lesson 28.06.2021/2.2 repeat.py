
first = int(input("put your first number"))
second=int(input("put your second number"))
math_mark=input("input operation ")
if math_mark=="+":
    print(first+second)
elif math_mark=="-":
    print(first-second)
elif math_mark=="*":
    print(first*second )
elif math_mark=="/":
    print(first/second)
